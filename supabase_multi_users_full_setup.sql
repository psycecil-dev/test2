to authenticated
with check (user_id = auth.uid()::text);

create policy "documents_update_own"
on public.documents
for update
to authenticated
using (user_id = auth.uid()::text)
with check (user_id = auth.uid()::text);

create policy "documents_delete_own"
on public.documents
for delete
to authenticated
using (user_id = auth.uid()::text);
